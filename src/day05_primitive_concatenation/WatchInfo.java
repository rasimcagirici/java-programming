package day05_primitive_concatenation;

public class WatchInfo {
    public static void main(String[] args){
        String brand = "Apple", color = "Black";
        double price = 1099;
        char gender = 'U';
        boolean waterResistant = true, isSmart = true;

        System.out.println("Brand:  " + brand + ".");
        System.out.println("Color:  " + color);
        System.out.println("Price:  $" + price);
        System.out.println("Gender: " + gender);
        System.out.println("Is it water resistant? " + waterResistant);
        System.out.println("Is it a smart watch? " + isSmart);
    }
}
